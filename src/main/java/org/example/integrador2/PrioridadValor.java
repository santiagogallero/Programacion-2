package org.example.integrador2;

public class PrioridadValor {
    private int prioridad;
    private int valor;

    public PrioridadValor(int prioridad, int valor) {
        this.prioridad = prioridad;
        this.valor = valor;
    }
    public int getPrioridad() {
        return prioridad;
    }
    public int getValor() {
        return valor;
    }

}
